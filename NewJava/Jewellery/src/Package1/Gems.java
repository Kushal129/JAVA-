package Package1;

public class Gems {
    private String gemId;
    private String gemName;

    public Gems(String gemId, String gemName) {
        this.gemId = gemId;
        this.gemName = gemName;
    }

    public String getGemId() {
        return gemId;
    }

    public String getGemName() {
        return gemName;
    }
}